export const BUILTIN_CONTENT_UPDATED_AT = BUILTIN_TIMESTAMP;

// Helper to get lesson content
export function getLessonContent(lessonId: number): BuiltinLessonContent | null {
  return BUILTIN_LESSON_CONTENT[lessonId as keyof typeof BUILTIN_LESSON_CONTENT] ?? null;
}

// Get specific module content for a lesson
export function getBuiltinModuleContent<M extends keyof ModuleContentMap>(
  lessonId: number,
  module: M
): ModuleContentMap[M] | null {
  const lesson = getLessonContent(lessonId);
  if (!lesson) return null;
  return lesson[module] ?? null;
}

// Helper to check if a lesson has content
export function hasLessonContent(lessonId: number): boolean {
  return lessonId in BUILTIN_LESSON_CONTENT;
}
