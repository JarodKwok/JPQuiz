const v = (
  word: string,
  reading: string,
  meaning: string,
  example?: string,
  kanji?: string
): VocabularyItem => ({
  word,
  reading,
  meaning,
  example,
  kanji,
});

const g = (
  id: string,
  name: string,
  meaning: string,
  connection: string,
  example: string,
  exampleTranslation: string,
  tip?: string
): GrammarItem => ({
  id,
  name,
  meaning,
  connection,
  example,
  exampleTranslation,
  tip,
});

const p = (
  id: string,
  pattern: string,
  meaning: string,
  structure: string,
  sampleJapanese: string,
  sampleReading: string,
  sampleTranslation: string,
  notes?: string
): SentencePatternItem => ({
  id,
  pattern,
  meaning,
  structure,
  sampleJapanese,
  sampleReading,
  sampleTranslation,
  notes,
});

const e = (
  japanese: string,
  reading: string,
  translation: string,
  grammar?: string
): ExampleItem => ({
  japanese,
  reading,
  translation,
  grammar,
});

const line = (
  japanese: string,
  translation: string,
  notes?: string
): TextLine => ({
  japanese,
  translation,
  notes,
});

const t = (title: string, lines: TextLine[]): TextContent => ({
  title,
  lines,
});

const q = (
  text: string,
  options: string[],
  answer: number
): ListeningItem => ({
  text,
  options,
  answer,
});

function lesson(content: {
  vocabulary: VocabularyItem[];
  grammar: GrammarItem[];
  patterns: SentencePatternItem[];
  examples: ExampleItem[];
  text: TextContent;
  listening: ListeningItem[];
}): BuiltinLessonContent {
  const examples: ExamplesContent = {
    patterns: content.patterns,
    examples: content.examples,
  };
  return {
    vocabulary: content.vocabulary,
    grammar: content.grammar,
    examples,
    text: content.text,
    listening: content.listening,
  };
}

